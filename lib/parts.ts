export type PartSegment = "new" | "used" | "machine"

export interface Part {
    id: string
    name: string
    partNumber: string
    brand: string // Manufacturer (e.g., Mobis, Bosch, Genuine)
    category: string
    subcategory: string
    segment: PartSegment // New field for top-level segmentation
    description: string
    priceKRW: number
    priceUSD: number
    compatibleVehicles: string[]
    condition: "New" | "Used" | "Refurbished" | "Remanufactured"
    stockStatus: "In Stock" | "Low Stock" | "Out of Stock"
    images: string[]
    rating: number
    reviewCount: number
    location?: string // Useful for used parts (e.g., "Incheon Yard 3")
}

